    private void tnamaCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_tnamaCaretUpdate
        // TODO add your handling code here:
        if (tnama.getText().length()==0 && tberat.getText().length()!=0 && tberat.getText().matches("[0]")){
           btnproses.setEnabled(false);
        }
        else if (tnama.getText().length()!=0){
           btnproses.setEnabled(true);
        }
    }//GEN-LAST:event_tnamaCaretUpdate

    private void tteleponCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_tteleponCaretUpdate
        // TODO add your handling code here:
        
        if (ttelepon.getText().length()==0 && !ttelepon.getText(). matches("[A-Z]") && !ttelepon.getText().matches("[a-z]")&& tberat.getText().matches("[0]") )  {
         btnproses.setEnabled(false);
        }
        else if (ttelepon.getText().length()!=0 && ttelepon.getText().matches("[1-9]")){
            btnproses.setEnabled(true);
        }
    }//GEN-LAST:event_tteleponCaretUpdate

    private void tberatCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_tberatCaretUpdate
        // TODO add your handling code here:
        Integer B;
        B = Integer.valueOf(tberat.getText());
        if(B==0){
            btnproses.setEnabled(false);
         }
        else if(B!=0){
            btnproses.setEnabled(true);
        }
    }//GEN-LAST:event_tberatCaretUpdate

