    private void btnprosesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnprosesActionPerformed
        // TODO add your handling code here:
        Float VDiskon;
        Nama = tnama.getText();
        NoHp = ttelepon.getText();
        Diskon=sliderdiskon.getValue();
        if(jreguler.isSelected()){
            jenis=this.jreguler.getText();
            VDiskon= Float.valueOf(sliderdiskon.getValue());
            Total=11500*(Float.valueOf(tberat.getText()))-((Diskon*11500)/100);
            uang=Total;
            
        }
        else if(jexpress.isSelected()){
             jenis=this.jexpress.getText();
            VDiskon= Float.valueOf(sliderdiskon.getValue());
            Total=11500*(Float.valueOf(tberat.getText()))-((Diskon*11500)/100);
            uang=(Total*1.5f);
            
        }
         if (tnama.getText().isEmpty()){
        JOptionPane.showMessageDialog(this, "Nama Anda Tidak Boleh Kosong\nSilahkan isi dulu","Informasi",
                JOptionPane.INFORMATION_MESSAGE);
        }
          if (ttelepon.getText().isEmpty()){
        JOptionPane.showMessageDialog(this, "Nomor Telepon Anda Tidak Boleh Kosong\nSilahkan isi dulu","Informasi",
                JOptionPane.INFORMATION_MESSAGE);
        }
           if (tberat.getText().isEmpty()){
        JOptionPane.showMessageDialog(this, "Berat Barang Anda Tidak Boleh Kosong\nSilahkan isi dulu","Informasi",
                JOptionPane.INFORMATION_MESSAGE);
    }
        tatotal.setText("|||||||=|TOTAL PEMBAYARAN||||||||"+
                          "\nNama \t: "+Nama+
                          "\nNo.Hp \t: "+NoHp+
                          "\nBerat Barang \t: "+tberat.getText()+"KG"+
                          "\nJenis \t: "+jenis+
                          "\nDiskon \t: "+Diskon+"%"+
                          "\nTotal \t: "+uang+"0" ); 
        
        
        
        
        
    }//GEN-LAST:event_btnprosesActionPerformed
